/**
 * Truncated VSOP87D coefficient tables.
 *
 * Theory: Bretagnon P. & Francou G., A&A 202 (1988) 309-315.
 * Data is in the public domain; coefficients here follow the truncated
 * presentation in Jean Meeus, "Astronomical Algorithms" 2nd ed. (1998).
 *
 * Each term is a 3-tuple [A, B, C] representing  A * cos(B + C * τ)
 * where τ = Julian millennia from J2000.0 = (JD - 2451545) / 365250.
 *
 * Amplitudes (A) are in units of 10⁻⁸:
 *   L series → 10⁻⁸ radians
 *   B series → 10⁻⁸ radians
 *   R series → 10⁻⁸ AU
 *
 * After summing each power series (L0 + L1·τ + L2·τ² + …), divide by 10⁸
 * to obtain the physical quantity in radians or AU.
 *
 * Coverage: Mercury, Venus, Earth, Mars, Jupiter, Saturn, Uranus, Neptune.
 * Pluto is not covered by VSOP87; callers must use a fallback for Pluto.
 *
 * Truncation criterion: terms with amplitude ≥ 10 × 10⁻⁸ are retained,
 * giving positional accuracy better than 1 arc-minute within ±5 Julian
 * centuries (±500 years) of J2000.
 */

// [amplitude, phase (rad), frequency (rad / Julian millennium)]
type Term = readonly [number, number, number]
type PowerSeries = readonly Term[]

export type Vsop87Series = {
  readonly L: readonly PowerSeries[]
  readonly B: readonly PowerSeries[]
  readonly R: readonly PowerSeries[]
}

// ---------------------------------------------------------------------------
// Naming convention: <planet><series><power>
//   series: L = longitude, B = latitude, R = radius
//   power:  0 = constant term, 1 = linear in τ, 2 = quadratic, …
// Example: earthL1 = the τ¹ coefficient array for Earth's longitude series.
// ---------------------------------------------------------------------------

